    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        try {
            if (jt_Vehiculo.getSelectedRow() >= 0) {
                int po = jt_Vehiculo.getSelectedRow();
                DefaultTableModel modelo = (DefaultTableModel) jt_Vehiculo.getModel();
                modelo.removeRow(po);
                jt_Vehiculo.setModel(modelo);
                adminCreados av = new adminCreados("./Vehiculos.txt");
                av.cargarArchivo();
                av.getVehiculo().remove(po);
                av.escribirArchivo();
            } else if (jt_Esamblador.getSelectedRow() >= 0) {
                int po = jt_Esamblador.getSelectedRow();
                DefaultTableModel modelo = (DefaultTableModel) jt_Esamblador.getModel();
                modelo.removeRow(po);
                jt_Esamblador.setModel(modelo);
                adminEsambladores ac = new adminEsambladores("./Esambladores.txt");
                ac.cargarArchivo();
                ac.getEsamblador().remove(po);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "OCURRIO UN ERROR");
        }
    }//GEN-LAST:event_eliminarActionPerformed

