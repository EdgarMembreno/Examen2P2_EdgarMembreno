    private void ListaDeVehiculosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListaDeVehiculosActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jt_Vehiculo.getModel();
        modelo.setRowCount(0);
        adminCreados av = new adminCreados("./Vehiculos.txt");
        av.cargarArchivo();
        for (int i = 0; i < av.getVehiculo().size(); i++) {
            String vin = av.getVehiculo().get(i).getVin();
            String color = av.getVehiculo().get(i).getColor();
            int asientos = av.getVehiculo().get(i).getAsientos();
            Object[] servicio = {vin, color, asientos};
            modelo.addRow(servicio);
        }
        jt_Vehiculo.setModel(modelo);
        DefaultTableModel model = (DefaultTableModel) jt_Esamblador.getModel();
        model.setRowCount(0);
        adminEsambladores ac = new adminEsambladores("./Esambladores.txt");
        ac.cargarArchivo();
        for (int i = 0; i < ac.getEsamblador().size(); i++) {
            String nombre = ac.getEsamblador().get(i).getNombre();
            int id = ac.getEsamblador().get(i).getId();
            int esamblados = ac.getEsamblador().get(i).getEsamblados();
            Object[] servicio = {nombre, id, esamblados};
            model.addRow(servicio);
        }
        jt_Esamblador.setModel(model);
        jd_Esamblar.setModal(true);
        jd_Esamblar.pack();
        jd_Esamblar.setLocationRelativeTo(this);
        jd_Esamblar.setVisible(true);


    }//GEN-LAST:event_ListaDeVehiculosActionPerformed

